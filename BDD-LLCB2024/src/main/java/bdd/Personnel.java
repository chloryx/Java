    private boolean checkEmptyField(){
        //Check si vide ou pas
        return (txtNom.getText().equals("") || txtPrenom.getText().equals("") || txtAdresse.getText().equals("") ||
                txtMail.getText().equals("") || txtTel.getText().equals(""));
    }
    
     public void Table() {
        String[] User = {"Nom", "Prénom", "Mail", "Téléphone", "Adresse", "Fonction"};
        String[] Monter = new String[6];

        tableau = new DefaultTableModel(null, User);

        try {
            conn = connecter.connBdd();
            ResultSet rs = requete.Tableau("select Nom, Prenom, Mail, Tel, Adresse, Metier from personnel, fonction where personnel.IDFonction = fonction.IDFonction", conn);
            while (rs.next()) {
                Monter[0] = rs.getString("Nom");
                Monter[1] = rs.getString("Prenom");
                Monter[2] = rs.getString("Mail");
                Monter[3] = rs.getString("Tel");
                Monter[4] = rs.getString("Adresse");
                Monter[5] = rs.getString("Metier");
                tableau.addRow(Monter);
            }
            TabPersonnel.setModel(tableau);
            conn.close();
        } catch (SQLException se) {
            se.printStackTrace();
        }
    }
    
