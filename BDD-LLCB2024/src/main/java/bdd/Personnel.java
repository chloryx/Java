    private void btnAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouterActionPerformed
        if (checkEmptyField()) {
            JOptionPane.showMessageDialog(null, "Les informations ne sont pas correctement complétées", "Information manquante", JOptionPane.ERROR_MESSAGE);
        }else{
            conn = connecter.connBdd();
            requete.AjoutePersonnel(txtNom.getText(), txtPrenom.getText(), txtAdresse.getText(), txtMail.getText(), txtTel.getText(), selectFonction.getSelectedIndex(), conn);
            connecter.deconnBdd(conn);
        }
    }//GEN-LAST:event_btnAjouterActionPerformed

